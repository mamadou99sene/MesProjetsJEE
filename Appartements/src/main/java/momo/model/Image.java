package momo.model;

public class Image 
{
	private int id;
	private String url;
	private int idApp;
	public int getId() {
		return id;
	}
	public String getUrl() {
		return url;
	}
	public int getIdApp() {
		return idApp;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public void setIdApp(int idApp) {
		this.idApp = idApp;
	}

}
